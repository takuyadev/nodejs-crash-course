const os = require('os')

// Platform
console.log(os.platform())

// CPU Arch
console.log(os.arch())

// CPU Core Info
console.log(os.cpus())

// Free memory
console.log(os.freemem())

// Total Memory
console.log(os.totalmem())

// Home Direcory
console.log(os.homedir())

// Computer uptime
console.log(os.uptime())